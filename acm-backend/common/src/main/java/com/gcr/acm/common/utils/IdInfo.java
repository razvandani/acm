package com.gcr.acm.common.utils;

/**
 * Container for id.
 *
 * @author Razvan Dani
 */
public class IdInfo {
    private Integer id;

    public IdInfo(Integer id) {
        this.id = id;
    }

    public IdInfo() {}

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
